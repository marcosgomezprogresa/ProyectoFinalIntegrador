const seedDatabase = async () => {
  try {
    const mongoURI = process.env.MONGODB_URI || 'mongodb://localhost:27017/recipes-db';

    await mongoose.connect(mongoURI, {
      useNewUrlParser: true,
      useUnifiedTopology: true,
    });

    console.log('âœ“ MongoDB connected');

    // Clear existing data
    await Recipe.deleteMany({});
    console.log('âœ“ Cleared existing recipes');

    // Insert recipes
    const inserted = await Recipe.insertMany(recipes);
    console.log(`âœ“ Inserted ${inserted.length} recipes`);

    console.log('\nðŸ“‹ Sample Recipes:');
    recipes.slice(0, 5).forEach((r, i) => {
      console.log(`  ${i + 1}. ${r.title} (${r.category})`);
    });

    await mongoose.connection.close();
    console.log('\nâœ“ Database seeded successfully!\n');
  } catch (error) {
    console.error('âœ— Error seeding database:', error.message);
    process.exit(1);
  }
};

seedDatabase();
